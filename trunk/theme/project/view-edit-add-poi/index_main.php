      Tag:historic=castle<br>
      The tag historic=castle is used in OpenStreetMap for various kinds of structures, 
      most of which were originally built as fortified residences of a lord or noble. 
      The scope of the tag currently also includes non-fortified residences like palaces; 
      and also fortresses, which were not always a residence for nobility. 
      Have a look at the key castle_type=* for common sub-types of this tag.
